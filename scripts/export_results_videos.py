"""Export original/full-heavy/roi-heavy frame folders into videos under results/video."""

from __future__ import annotations

import argparse
from pathlib import Path
import sys

ROOT = Path(__file__).resolve().parents[1]
if str(ROOT) not in sys.path:
    sys.path.insert(0, str(ROOT))

from src.io_utils import ensure_dir
from src.video_export import export_results_videos, export_side_by_side_video


def parse_args() -> argparse.Namespace:
    parser = argparse.ArgumentParser(description="Export result frame folders to videos")
    parser.add_argument(
        "--input-frames-dir",
        default="../BasicVSR_PlusPlus/data/demo_000",
        help="Original input frame directory",
    )
    parser.add_argument(
        "--full-heavy-frames-dir",
        default="results/output/full_heavy",
        help="Frame directory generated by full_heavy",
    )
    parser.add_argument(
        "--roi-heavy-frames-dir",
        default="results/output/roi_heavy",
        help="Frame directory generated by roi_heavy",
    )
    parser.add_argument(
        "--results-video-dir",
        default="results/video",
        help="Directory to save output videos",
    )
    parser.add_argument(
        "--fps",
        type=float,
        default=25.0,
        help="FPS used when writing videos",
    )
    parser.add_argument(
        "--make-side-by-side",
        action="store_true",
        help="Also create a side-by-side comparison video",
    )
    return parser.parse_args()


def main() -> int:
    args = parse_args()
    results_video_dir = ensure_dir(args.results_video_dir)

    outputs = export_results_videos(
        input_frames_dir=args.input_frames_dir,
        full_heavy_frames_dir=args.full_heavy_frames_dir,
        roi_heavy_frames_dir=args.roi_heavy_frames_dir,
        results_video_dir=results_video_dir,
        fps=args.fps,
    )

    print("已导出视频：")
    for name, path in outputs.items():
        print(f"- {name}: {path}")

    if args.make_side_by_side:
        side_by_side_path = results_video_dir / "original_vs_full_heavy.mp4"
        export_side_by_side_video(
            left_frames_dir=args.input_frames_dir,
            right_frames_dir=args.full_heavy_frames_dir,
            output_video_path=side_by_side_path,
            fps=args.fps,
        )
        print(f"- side_by_side: {side_by_side_path}")

    return 0


if __name__ == "__main__":
    raise SystemExit(main())
